namespace Core
{
    /// <summary>
    /// Represents different types of log messages.
    /// </summary>
    public enum LogType
    {
        /// <summary>
        /// Informational log message.
        /// </summary>
        Info,

        /// <summary>
        /// Warning log message.
        /// </summary>
        Warn,

        /// <summary>
        /// Error log message.
        /// </summary>
        Error,

        /// <summary>
        /// Fatal log message.
        /// </summary>
        Fatal,

        /// <summary>
        /// Debugging log message.
        /// </summary>
        Debug
    }
}
